package com.cabletech.business.workflow.workorder.model;

import com.cabletech.common.base.BaseEntity;

/**
 * 通用工单签收实体
 * 
 * @author 杨隽 2012-01-06 创建
 * 
 */
public class WorkOrderSignFor extends BaseEntity {
	/**
	 * 序列化编号
	 */
	private static final long serialVersionUID = 1L;
	// 工单ID
	private String taskId;
	// 工作流流程的业务编号
	private String workflowBzId;
	// 签收人
	private String signForUserId;
	// 签收结果
	private String signForResult;
	// 签收意见
	private String signForRemark;
	// 巡检组编号
	private String patrolGroupId;
	// 工作流当前任务编号
	private String workflowTaskId;

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getWorkflowBzId() {
		return workflowBzId;
	}

	public void setWorkflowBzId(String pId) {
		this.workflowBzId = pId;
	}

	public String getSignForUserId() {
		return signForUserId;
	}

	public void setSignForUserId(String signForUserId) {
		this.signForUserId = signForUserId;
	}

	public String getSignForResult() {
		return signForResult;
	}

	public void setSignForResult(String signForResult) {
		this.signForResult = signForResult;
	}

	public String getSignForRemark() {
		return signForRemark;
	}

	public void setSignForRemark(String signForRemark) {
		this.signForRemark = signForRemark;
	}

	public String getPatrolGroupId() {
		return patrolGroupId;
	}

	public void setPatrolGroupId(String patrolGroupId) {
		this.patrolGroupId = patrolGroupId;
	}

	public String getWorkflowTaskId() {
		return workflowTaskId;
	}

	public void setWorkflowTaskId(String workflowTaskId) {
		this.workflowTaskId = workflowTaskId;
	}
}
