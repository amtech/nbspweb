package com.cabletech.business.workflow.workorder.model;

import com.cabletech.common.base.BaseEntity;

/**
 * 通用工单回单验证实体
 * 
 * @author 杨隽 2012-01-06 创建
 * @author 杨隽 2012-02-24 添加“转审人”变量
 * 
 */
public class WorkOrderReplyCheck extends BaseEntity {
	/**
	 * 序列化编号
	 */
	private static final long serialVersionUID = 1L;
	// 工单ID
	private String taskId;
	// 工作流流程的业务编号
	private String workflowBzId;
	// 验证人
	private String checkUserId;
	// 验证结果
	private String checkResult;
	// 验证意见
	private String checkRemark;
	// 转审人
	private String transferApprover;
	// 工作流当前任务编号
	private String workflowTaskId;
	// 短信发送接收人编号
	private String acceptMessageUserId;

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getWorkflowBzId() {
		return workflowBzId;
	}

	public void setWorkflowBzId(String pId) {
		this.workflowBzId = pId;
	}

	public String getCheckUserId() {
		return checkUserId;
	}

	public void setCheckUserId(String signForUserId) {
		this.checkUserId = signForUserId;
	}

	public String getCheckResult() {
		return checkResult;
	}

	public void setCheckResult(String signForResult) {
		this.checkResult = signForResult;
	}

	public String getCheckRemark() {
		return checkRemark;
	}

	public void setCheckRemark(String signForRemark) {
		this.checkRemark = signForRemark;
	}

	public String getTransferApprover() {
		return transferApprover;
	}

	public void setTransferApprover(String transferApprover) {
		this.transferApprover = transferApprover;
	}

	public String getWorkflowTaskId() {
		return workflowTaskId;
	}

	public void setWorkflowTaskId(String workflowTaskId) {
		this.workflowTaskId = workflowTaskId;
	}

	public String getAcceptMessageUserId() {
		return acceptMessageUserId;
	}

	public void setAcceptMessageUserId(String acceptMessageUserId) {
		this.acceptMessageUserId = acceptMessageUserId;
	}
}
