package com.cabletech.business.base.service;

import com.cabletech.business.base.model.CodeSequence;

/**
 * 序号生成记录信息业务接口
 * 
 * @author 杨隽 2012-01-12 创建
 * 
 */
public interface CodeSequenceService {
	/**
	 * 根据组织编号和年月获取序号生成记录信息
	 * 
	 * @param codeSequence
	 *            CodeSequence 序号生成记录信息
	 * @return CodeSequence 序号生成记录信息
	 */
	CodeSequence getCodeSequence(CodeSequence codeSequence);

	/**
	 * 保存序号生成记录信息
	 * 
	 * @param codeSequence
	 *            CodeSequence 序号生成记录信息
	 */
	void save(CodeSequence codeSequence);
}
