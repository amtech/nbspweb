package com.cabletech.business.ah.excelreport.model;

import com.cabletech.common.base.BaseEntity;

/**
 * 
 * 地市每个月上传的excel报表
 * 
 * @author 杨隽 2012-06-27 创建
 * 
 */
public class AhExcelReportSheet extends BaseEntity {
	/**
	 * 不需要进行汇总的sheet类型
	 */
	public static final String SHEET_TYPE_ZERO = "0";
	/**
	 * 包含合计行进行汇总的sheet类型
	 */
	public static final String SHEET_TYPE_ONE = "1";
	/**
	 * 不包含合计行进行汇总的sheet类型
	 */
	public static final String SHEET_TYPE_TWO = "2";
	/**
	 * 按地市汇总类型
	 */
	public static final String CITY_SUM_TYPE = "0";
	/**
	 * 按全部汇总类型
	 */
	public static final String PROVINCE_SUM_TYPE = "1";
	/**
	 * 序列化编号
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * sheet页号
	 */
	private int sheetNum;
	/**
	 * sheet名
	 */
	private String sheetName;
	/**
	 * 规则号
	 */
	private String flg;
	/**
	 * 开始行
	 */
	private int row;
	/**
	 * 开始列
	 */
	private int col;

	public int getSheetNum() {
		return sheetNum;
	}

	public void setSheetNum(int sheetNum) {
		this.sheetNum = sheetNum;
	}

	public String getSheetName() {
		return sheetName;
	}

	public void setSheetName(String sheetName) {
		this.sheetName = sheetName;
	}

	public String getFlg() {
		return flg;
	}

	public void setFlg(String flg) {
		this.flg = flg;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}
}