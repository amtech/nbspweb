package com.cabletech.business.ah.rating.model;

import java.util.Date;

import com.cabletech.common.base.BaseEntity;

/**
 * 考核表
 * 
 * @author 杨隽 2012-06-26 创建
 */
public class RatingForm extends BaseEntity {
	private static final long serialVersionUID = 1L;
	// 巡检项目启用状态
	public static final String ITEM_START_USING_STATE = "1";
	// 巡检项目作废状态
	public static final String ITEM_SCRAP_STATE = "2";
	/**
	 * 系统编号
	 */
	private String id;
	/**
	 * 考核表标题
	 */
	private String title;
	/**
	 * 考核表说明
	 */
	private String remark;
	/**
	 * 启用状态
	 */
	private String useState;
	/**
	 * 专业
	 */
	private String businessType;
	/**
	 * 创建人
	 */
	private String creater;
	/**
	 * 创建时间
	 */
	private Date createTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getUseState() {
		return useState;
	}

	public void setUseState(String useState) {
		this.useState = useState;
	}

	public String getBusinessType() {
		return businessType;
	}

	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public String getCreater() {
		return creater;
	}

	public void setCreater(String creater) {
		this.creater = creater;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
}