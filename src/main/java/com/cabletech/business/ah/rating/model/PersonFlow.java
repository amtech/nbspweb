package com.cabletech.business.ah.rating.model;

import com.cabletech.common.base.BaseEntity;

/**
 * 
 * 人员流程定义
 * 
 * @author wj
 * 
 */
public class PersonFlow extends BaseEntity{

	private String id;;// id
	private String personId;// 人员id
	private String processer;// 处理人
	private int flowNum;// 流程序号
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPersonId() {
		return personId;
	}
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	public String getProcesser() {
		return processer;
	}
	public void setProcesser(String processer) {
		this.processer = processer;
	}
	public int getFlowNum() {
		return flowNum;
	}
	public void setFlowNum(int flowNum) {
		this.flowNum = flowNum;
	}
}