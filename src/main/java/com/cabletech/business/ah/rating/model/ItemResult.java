package com.cabletech.business.ah.rating.model;

import com.cabletech.common.base.BaseEntity;

/**
 * 月考核项目结果
 * @author wangt
 *
 */
public class ItemResult extends BaseEntity {
	private String id;
	private String examid;
	private String itemid;
	private String targetcompletevalue;
	private String selfscore;
	private String selfremark;
	private String score;
	private String examremark;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getExamid() {
		return examid;
	}
	public void setExamid(String examid) {
		this.examid = examid;
	}
	public String getItemid() {
		return itemid;
	}
	public void setItemid(String itemid) {
		this.itemid = itemid;
	}
	public String getTargetcompletevalue() {
		return targetcompletevalue;
	}
	public void setTargetcompletevalue(String targetcompletevalue) {
		this.targetcompletevalue = targetcompletevalue;
	}
	public String getSelfscore() {
		return selfscore;
	}
	public void setSelfscore(String selfscore) {
		this.selfscore = selfscore;
	}
	public String getSelfremark() {
		return selfremark;
	}
	public void setSelfremark(String selfremark) {
		this.selfremark = selfremark;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getExamremark() {
		return examremark;
	}
	public void setExamremark(String examremark) {
		this.examremark = examremark;
	}

}
