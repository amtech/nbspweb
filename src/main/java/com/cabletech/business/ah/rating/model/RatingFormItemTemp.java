package com.cabletech.business.ah.rating.model;

/**
 * 考核表EXCEL解析临时对象 非持久化
 * 
 * @author 杨隽 2012-06-26 创建
 */
public class RatingFormItemTemp {
	/**
	 * 考核表标题
	 */
	private String title;
	/**
	 * 考核表专业
	 */
	private String businessType;
	/**
	 * 考核项目
	 */
	private String item;
	/**
	 * 权重
	 */
	private String weight;
	/**
	 * 基本值
	 */
	private String baseValue;
	/**
	 * 挑战值
	 */
	private String challengeValue;
	/**
	 * 指标说明
	 */
	private String normRemark;
	/**
	 * 评价标准
	 */
	private String evaluationCriterion;
	/**
	 * 导入创建人编号
	 */
	private String creater;
	/**
	 * 导入是否成功标志
	 */
	private boolean importSuccessFlag = true;
	/**
	 * 错误提示信息
	 */
	private String errorMsg = "";

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getBusinessType() {
		return businessType;
	}

	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getBaseValue() {
		return baseValue;
	}

	public void setBaseValue(String baseValue) {
		this.baseValue = baseValue;
	}

	public String getChallengeValue() {
		return challengeValue;
	}

	public void setChallengeValue(String challengeValue) {
		this.challengeValue = challengeValue;
	}

	public String getNormRemark() {
		return normRemark;
	}

	public void setNormRemark(String normRemark) {
		this.normRemark = normRemark;
	}

	public String getEvaluationCriterion() {
		return evaluationCriterion;
	}

	public void setEvaluationCriterion(String evaluationCriterion) {
		this.evaluationCriterion = evaluationCriterion;
	}

	public String getCreater() {
		return creater;
	}

	public void setCreater(String creater) {
		this.creater = creater;
	}

	public boolean isImportSuccessFlag() {
		return importSuccessFlag;
	}

	public void setImportSuccessFlag(boolean flag) {
		this.importSuccessFlag = flag;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
}
